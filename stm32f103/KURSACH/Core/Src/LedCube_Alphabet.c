uint16_t LedCube_FindSymbol(uint16_t symbol)			//Поиск символа в алфавите
{
	if(symbol >= 'а' && symbol <= 'я')			//Перевод символа в верхний регистр рус
	{
		symbol += 'А' - 'а';
	}
	if(symbol >= 'a' && symbol <= 'z')			//Перевод символа в верхний регистр англ
	{
		symbol += 'A' - 'a';
	}
	for(int i = 0; i < NUMBER_OF_SYMBOLS; ++i)
	{
		if (SymbolsQueue[i] == symbol)
		{
			return i;
		}
	}

	return OFF;
}
